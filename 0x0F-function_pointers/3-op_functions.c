/**
 * op_add - sums two numbers
 *
 * @a: first int
 * @b: second int
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 *
 * @a: first int
 * @b: second int
 *
 * Return: difference between a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 *
 * @a: first int
 * @b: second int
 *
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - integer division of two numbers
 *
 * @a: first int
 * @b: second int
 *
 * Return: quotient of dividing a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modular division of two numbers
 *
 * @a: first int
 * @b: second int
 *
 * Return: remainder from dividing a by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
